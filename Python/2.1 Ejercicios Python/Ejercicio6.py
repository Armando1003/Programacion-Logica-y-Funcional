import random

print("Funciones")

# Esta función genera un número aleatorio entre 1 y 9
numero_random = random.randint(1, 9)
print("Número aleatorio:", numero_random)

# Esta función genera un número aleatorio entre 0 y 1
num = random.randint(0, 1)
print("Número aleatorio:", num)

# Esta función calcula el valor absoluto de un número
print("Valor Absoluto:", abs(-10))

# Esta función calcula el valor máximo de dos números a y b
print("Valor Máximo:", max(3, 5))

# Esta función calcula el valor mínimo de dos números a y b
print("Valor Mínimo:", min(-1, 7))

# Esta función calcula la longitud de una lista
lista_frutas = ['manzana', 'plátano', 'fresa']
print("Longitud:", len(lista_frutas))

# Esta función calcula la potencia de un número a elevado a la potencia b
print("Potencia:", pow(2, 8))

# Esta función redondea un número a un número entero
print("Número redondeado:", round(5.6))